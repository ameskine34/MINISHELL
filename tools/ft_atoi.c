#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		signe;
	int		result;
	int		i;
	char	*str;

	str = (char *)nptr;
	i = 0;
	signe = 1;
	result = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		result = (result * 10) + (str[i] - 48);
		i++;
	}
	return (result * signe);
}
